public class RelationShip {
    public String trust;
    public String care;
    public String openness;
    public String communication;
    public String love;


    public RelationShip(String loyalty, String lookAfter, String truthful, String communicate, String luv) {
        this.trust = loyalty;
        this.care = lookAfter;
        this.openness = truthful;
        this.communication = communicate;
        this.love = luv;

    }
}
